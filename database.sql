    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    code VARCHAR(50) UNIQUE NOT NULL,
    price INT NOT NULL,
    stock INT NOT NULL DEFAULT 0
);

